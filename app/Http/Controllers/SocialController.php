    public function getSocial()
    {
        $socials = Social::all();
        $info = [];
        foreach($socials as $social){
            $info[$social->name]['id'] = $social->id;
            $info[$social->name]['name'] = $social->name;
            $info[$social->name]['link'] = $social->link;
        }
        return $info;
    }
